def main():
    path = "data/스킬컨셉 NEW.xlsx"
    df = pd.read_excel(path, header=0)
    original_cols = list(df.columns)
    df.columns = ['성급', '속성', '직업', '캐릭터', '스킬타입', '스킬설명', '강화1', '강화2', '비고']

    filled_count = 0
    not_found = []

    for idx, row in df.iterrows():
        if pd.isna(row['스킬설명']):
            key = (row['캐릭터'], row['스킬타입'])
            if key in SKILL_DESIGNS:
                df.at[idx, '스킬설명'] = SKILL_DESIGNS[key]
                filled_count += 1
            else:
                not_found.append(f"  {row['캐릭터']} ({row['속성']}/{row['직업']} {row['성급']}★) - {row['스킬타입']}")

    # 원래 컬럼명 복원
    df.columns = original_cols

    df.to_excel(path, index=False)
    print(f"채워진 빈칸: {filled_count}개")

    remaining = df[df.iloc[:, 5].isna()]
    print(f"남은 빈칸: {len(remaining)}개")

    if not_found:
        print(f"\n매칭 실패 ({len(not_found)}건):")
        for nf in not_found:
            print(nf)


if __name__ == "__main__":
    main()
