import pandas as pd


def load():
    path = "data/스킬컨셉 NEW.xlsx"
    df = pd.read_excel(path, header=0)
    original_cols = list(df.columns)
    df.columns = ['성급', '속성', '직업', '캐릭터', '스킬타입', '스킬설명', '강화1', '강화2', '비고']
